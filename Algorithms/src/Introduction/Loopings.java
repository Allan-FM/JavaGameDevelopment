package Introduction;

public class Loopings {
    public static void main(String[] args) {
        int cout = 0;
        /*
         * while (cout <= 10) {
         * System.out.println(cout);
         * cout++;
         * }
         */
        /*
         * for (int i = 0; i <= 10; i++) {
         * System.out.println(i);
         * }
         */
        do{
            System.out.println(cout);
            cout++;
        }while(cout <= 10);

    }
}
